class SPPF_EMA(nn.Module):
    """Spatial Pyramid Pooling - Fast (SPPF) layer for YOLOv5 by Glenn Jocher."""

    def __init__(self, c1, c2,  k=5):  # equivalent to SPP(k=(5, 9, 13))
        super().__init__()
        c_ = c1 // 2  # hidden channels
        #self.cv1 = Conv(c1, c_, 1, 1)
        #self.cv2 = Conv(c_ * 4, c2, 1, 1)
        #self.attention1 = SelfAttention(c_)
        self.cv1 = GhostModule(c1, c_, 1, 2)
        self.attention1 = SelfAttention(c_)
        self.cv2 = GhostModule(c_ * 4, c2, 1, 2)
        self.m = nn.MaxPool2d(kernel_size=k, stride=1, padding=k // 2)
        self.attention2 = SelfAttention(c2)


    def forward(self, x):
        """Forward pass through Ghost Convolution block."""
        x = self.attention1(self.cv1(x))
        #x = self.cv1(x)
        y1 = self.m(x)
        y2 = self.m(y1)
        #return self.attention(self.cv2(torch.cat((x, y1, y2, self.m(y2)), 1)))
        return self.cv2(self.attention2(torch.cat((x, y1, y2, self.m(y2)), 1)))

class SPPCSPC(nn.Module):
    # CSP https://github.com/WongKinYiu/CrossStagePartialNetworks
    def __init__(self, c1, c2, n=1, shortcut=False, g=1, e=0.5, k=(5, 9, 13)):
        super(SPPCSPC, self).__init__()
        c_ = int(2 * c2 * e)  # hidden channels
        self.cv1 = Conv(c1, c_, 1, 1)
        self.cv2 = Conv(c1, c_, 1, 1)
        self.cv3 = Conv(c_, c_, 3, 1)
        self.cv4 = Conv(c_, c_, 1, 1)
        self.m = nn.ModuleList([nn.MaxPool2d(kernel_size=x, stride=1, padding=x // 2) for x in k])
        self.cv5 = Conv(4 * c_, c_, 1, 1)
        self.cv6 = Conv(c_, c_, 3, 1)
        self.cv7 = Conv(2 * c_, c2, 1, 1)

    def forward(self, x):
        x1 = self.cv4(self.cv3(self.cv1(x)))
        y1 = self.cv6(self.cv5(torch.cat([x1] + [m(x1) for m in self.m], 1)))
        y2 = self.cv2(x)
        return self.cv7(torch.cat((y1, y2), dim=1))
